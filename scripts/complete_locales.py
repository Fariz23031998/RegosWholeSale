LOCALES = {
    "ru": LOCALE_RU,
    "uz": LOCALE_UZ,
    "tj": LOCALE_TJ,
}


def load_en() -> dict[str, str]:
    data = json.loads(EN_PATH.read_text(encoding="utf-8"))
    return data["translations"]


def count_english_fallbacks(en: dict[str, str], locale: dict[str, str]) -> list[str]:
    allowed = LANGUAGE_KEYS | BRAND_KEYS
    return [k for k, v in locale.items() if v == en[k] and k not in allowed]


def write_locale(lang: str, translations: dict[str, str]) -> Path:
    payload = {
        "version": VERSION,
        "last_updated": date.today().isoformat(),
        "translations": translations,
    }
    path = OUT_DIR / f"{lang}.json"
    path.write_text(json.dumps(payload, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")
    LOCALES_OVERRIDE_DIR.mkdir(parents=True, exist_ok=True)
    override_path = LOCALES_OVERRIDE_DIR / f"{lang}.json"
    override_path.write_text(json.dumps(translations, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")
    return path


def main() -> int:
    en = load_en()
    en_keys = set(en.keys())
    exit_code = 0

    for lang, locale in LOCALES.items():
        locale_keys = set(locale.keys())
        if locale_keys != en_keys:
            missing = sorted(en_keys - locale_keys)
            extra = sorted(locale_keys - en_keys)
            print(f"ERROR {lang}: key mismatch (missing={len(missing)}, extra={len(extra)})", file=sys.stderr)
            if missing:
                print(f"  missing: {missing[:5]}...", file=sys.stderr)
            exit_code = 1
            continue

        fallbacks = count_english_fallbacks(en, locale)
        if fallbacks:
            print(f"ERROR {lang}: {len(fallbacks)} English fallbacks", file=sys.stderr)
            for k in fallbacks[:10]:
                print(f"  {k}", file=sys.stderr)
            exit_code = 1
            continue

        path = write_locale(lang, locale)
        translated = sum(1 for k, v in locale.items() if v != en[k])
        print(f"Wrote {path} ({len(locale)} keys, {translated} non-English translations)")

    subprocess.run([sys.executable, str(Path(__file__).parent / "gen_fallback_translations.py")], check=True)

    return exit_code


if __name__ == "__main__":
    raise SystemExit(main())
